namespace TopDownProteomics.ProForma
{
    /// <summary>
    /// Possible keys for a ProFormaDescriptor
    /// </summary>
    public enum ProFormaKey
    {
        /// <summary>
        /// The modification
        /// </summary>
        Mod,

        /// <summary>
        /// The mass
        /// </summary>
        Mass,

        /// <summary>
        /// The formula (in Unimod notation)
        /// </summary>
        Formula,

        /// <summary>
        /// The user defined extra information
        /// </summary>
        Info
    }
}